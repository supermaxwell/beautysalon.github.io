                    <h3 class="section-title">Чистка лица</h3>
                   <img class="img-circle"    src="dummy/lico.jpg" alt=""/>
						<strong>Чистка лица</strong> это косметологическая процедура, которая позволяет очистить поверхность кожи от отмершего эпителия, комедонов, воспаленных угрей, милиумов, кожного сала и т.д. В результате поры очищаются, за счет улучшения кровообращения выравнивается цвет, разглаживается рельеф, выводятся шлаки и токсины. Чистка лица помогает подготовить кожу к дальнейшим увлажняющим, омолаживающим, питательным и отбеливающим процедурам.<br><br>
                      <strong>Различают 3 основновных вида чистки лица</strong>

<ul class="prices">


   <li> Ультразвуковая чистка лица</li>
  <li>  Мануальная чистка лица</li>
    <li>Комбинированная чистка лица</li>


    </ul>

<h3 class="feature-title">Ультразвуковая чистка лица</h3>
Это аппаратный метод воздействия на кожу при помощи ультразвукового скрабера.Ультразвуковая чистка лица позволяет делекатно и эффективно очистить кожу от так называемых черных точек (поверхностных комедонов), закупоренных сальных желез и кожного жира, не травмируя её.
<br>
<br>


 <strong>Показания для ультразвуковой чистки:</strong>
 <ul class="prices">
 <li>наличие черных точек и комедонов;</li>
   <li> угревая сыпь (но не в стадии обострения);</li>
  <li>  жирная, увядающая, комбинированная кожа;</li>
  <li>  расширенные поры;</li>
    <li>тусклый цвет лица. <br></li>
    </ul>

    <strong>Частота проведения.</strong><br>
<br>
 Для жирной кожи требуется проводить ультразвуковую чистку один раз в 1-2 в месяц. Для сухой кожи раз в 3 месяца.
<br>
<br>

 <strong>Эффект от процедуры:</strong> <br>
 <ul class="prices">
 <li>тщательная, нежная эксфолиация;</li>
    <li>глубокое очищение пор;</li>
    <li>улучшение цвета и тонуса кожи;</li>
   <li> выравнивание рельефа кожи;</li>
   <li> стимуляция лимфатического дренажа;</li>
    <li>уменьшение отечности;</li>
    <li>активизация обменных процессов.</li>
    </ul>

 <strong>Рекомендации по уходу за кожей после проведения процедуры.</strong><br>
<br>
Ультразвуковая чистка лица не требует реабилитации, так как раздражения и покраснения на коже не возникает.
<br>
<br>

<strong>Противопоказания для ультразвуковой чистки лица</strong>:<br>

 <ul class="prices">
   <li> экзема и гнойничковые заболевания кожи (к примеру, фурункулы), герпес,</li>
    <li>гипертоническая болезнь сильной степени тяжести,</li>
    <li>онкология,</li>
    <li>неврология тройничного и глазничного нервов,</li>
   <li> инфекционные заболевания,</li>
   <li> расширение сосудов (усиление капиллярного рисунка).</li>
   </ul>


<h3 class="feature-title">Мануальная (ручная, механическая) чистка лица</h3>
 Для проведения процедуры используются специальные стерильные инструменты и непосредственно руки косметолога.
<br>
<br>

<strong>Показания для мануальной чистки:</strong>
<br>

<ul class="prices">черные точки (комедоны), сальные пробки;
    <li>тусклый цвет кожи лица;</li>
   <li> закупоренные/расширенные поры;</li>
  <li>  угревая сыпь (созревшие пустулы);</li>
    <li>милиумы (просянки).</li>
    </ul>

<strong>Частота проведения.</strong> <br>
<br>
Для нормальной/сухой кожи 1 раз в 3-6 мес. Комбинированная кожа нуждается в чистке примерно 1 раз в 2-3 мес. Жирная/проблемная кожа – 1 раз в 1-1,5 мес.
<br>
<br>
 <strong>Эффект от процедуры:</strong><br>

<ul class="prices">
  <li> выравнивание цвета кожи;</li>
    <li>повышение кожного иммунитета;</li>
    <li>очищение пор;</li>
    <li>усиление регенерации тканей.</li>
    <li>дренирование протоков сальных желез;</li>
    <li>активизация обмена веществ.</li>
  </ul>

<strong>Рекомендации по уходу за кожей после проведения процедуры.</strong> На кожи, на протяжении нескольких часов, могут оставаться покраснения, которое вскоре проходят. Первые 2-3 дня нельзя загорать и купаться, это может привести к пигментации, также не рекомендуется в течение 12 часов умывать кожу.
<br>
<br>

<strong>Противопоказания для мануальной чистки лица:</strong><br>

<ul class="prices">
  <li>наличие в большом количестве воспаленных высыпаний на коже,</li>
  <li>герпес, дерматит, фурункулез, экзема в активной форме,</li>
<li>  менструация,</li>
 <li> онкология,</li>
<li>  неврология тройничного и глазничного нервов,</li>
 <li> инфекционные заболевания.</li>
</ul>

<h3 class="feature-title">Комбинированная чистка лица:</h3>
Самый эффективный метод очищения кожи, для профилактики угревой сыпи, прыщей и купероза.Процедура совмещает в себе ультразвуковой и механический метод чистки лица; удается глубоко очистить сильно загрязненные области и бережно обработать чувствительные зоны.<br>
<br>

 <strong>Показания для комбинированной чистки:</strong><br>


<ul class="prices">
  <li>угревая сыпь;</li>
  <li>пористая кожа;</li>
  <li>жирная, комбинированная, увядающая кожа;</li>
 <li> наличие мелких дефектов кожи;</li>
  <li>наличие «черных точек» (комедонов);</li>
 <li> неровный цвет лица;</li>
  <li>сниженный тонус и тургор кожи.</li>
</ul>

<strong> Частота проведения.</strong> Для нормальной/сухой кожи 1 раз в 3-6 мес. Комбинированная кожа нуждается в чистке примерно 1 раз в 2-3 мес. Жирная/проблемная кожа –1 раз в 1-2 мес
<br>
<br>

<strong>Эффект от процедуры. </strong>Здоровая, гладкая, матовая, чистая, нежная кожа с ровным цветом лица и суженными порами, без высыпаний, воспалений, милиумов и сальных пробок.
<br>
<br>

<strong>Рекомендации по уходу за кожей после проведения процедуры.</strong> Рекомендации такие же как и при мануальной чистке. На коже после процедуры несколько часов остается покраснение. Первые 2-3 дня нельзя загорать и купаться, в течение 12 часов - умывать кожу.<br>
<br>
<strong>Противопоказания для комбинированной чистки лица:</strong><br>


<ul class="prices">

    <li>экзема, гнойничковые заболевания кожи (фурункулы), герпес, дерматит</li>
  <li>  гипертоническая болезнь сильной степени тяжести,</li>
    <li>расширение сосудов (усиление капиллярного рисунка)<./li>
   <li> менструация,</li>
  <li>  онкология,</li>
