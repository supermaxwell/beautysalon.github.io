						<p> <b>Фотоэпиляция</b> -  это метод, который позволяет навсегда избавится от нежелательных волос, благодаря использованию высокоимпульсного света и тепловых волн.</p>
<h3 class="feature-title">Суть метода</h3>

<p>  <b>Меланин </b>– природный темный пигмент, который содержится в коже и волосах, способен поглощать световые волны. Под воздействием тепловых волн происходит нагрев ткани, в следствии чего волосяной фоликул разрушается, а волос просто отпадает.
<br>
<br>
Процедура фотоэпиляции пользуется популярностью как у женщин, так и у мужчин.</p>

<h3 class="feature-title">Преимущества фотоэпиляции</h3>
Фотоэпиляция - это не инвазивный метод воздействия на кожный покров, который гарантирует безопасность с точки зрения инфицирования кожи. Благодаря специальной системе охлаждения и правильно подобранному фильтру обеспечивается максимальная безболезненность процедуры.
<br>
<br>
Быстрота  проведения достигается благодаря тому, что уже за одну вспышку обрабатывается участок тела площадью 6,3 см2, таким образом длительность процедуры значительно сокращается в сравнении с аналогичной лазерной эпиляцией.
<br>
<br>
Также, стоить отметить, что фотоэпиляция благотворно влияет на кожу, способствует выработке коллагена, который отвечает за молодость и упругость нашей кожи.


<h3 class="feature-title">Результаты после проведения</h3>
Уже после первой процедуры Вы заметите, что волос стало значительно меньше. Обычно, выпадает около 20% волос. Оставшиеся волосы выпадают не столь «массово», а понемногу после каждой процедуры.
<br>
<br>
Для проведения процедуры длина волос должна быть 1—5 мм.
<br>
<br>
Длительность процедуры: в зависимости от эпилируемой зоны, сеанс может продолжаться от 10-ти до 40 минут.
<br>
<br>
Восстановительный период: сразу после процедуры наблюдается покраснение обработанных участков кожи, иногда отечность.
<br>
<br>
Длительность реабилитационного периода составляет 5-7 дней.
<br>
<br>
В первые дни, после процедуры желательно исключить использование косметики, не рекомендовано посещать бассейн, сауну, баню.
<br>
<br>
Процедура фотоэпиляции не проводится в период солнечной активности (летом), а также на загоревшей коже, так как это может спровоцировать ожог, вызвать волдыри и изменить цвет кожи.
<br>
<br>
После процедуры (в течении 2 недель) следует сократить время нахождения на солнце, а в случаях пребывания под ультрафиолетовыми лучами необходимо пользоваться солнцезащитными средствами с высоким уровнем защиты (SPF 50+).
<br>
<br>
Количество необходимых процедур для достижения максимального результата может колебаться от 5 до 12 процедур, и зависит от типа кожи и цвета волос.
<br>
<br>
<b>Рекомендуемая частота проведения процедуры – 3-4 недели, в зависимости от быстроты роста волос.</b>




