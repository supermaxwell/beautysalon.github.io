     <h3 class="feature-title">Фотоомоложение лица. Технология выполнения.</h3>

Фотоомоложение проводят с помощью специального прибора, излучающего широкий спектр световой энергии, испускаемой короткими порциями вспышками. В нашем медицинском центре доктора проводят процедуру на аппарате NannoLight™ IPL System<br>


<b>Длительность сеанса:</b> зависит от зоны, на которой проводят процедуру, и составляет около 15–40 минут.

<h4>Уход и рекомендации до и после фототерапии.</h4>

Процедуру фотоомложения, как и любую другую фото и лазерную процедуру нельзя делать на загоревшую кожу и в период солнечной активности (летом), так как это может привести к ожогам тканей.

За 3 дня до и после процедуры также не стоит посещать сауну и места с хлориванной водой (басейн, аквапарк и т.д.).

За несколько дней до и после процедуры, на обрабатываемое место не рекомендовано наносить любые спиртсодержащие средства

После фототерапии возможно небольшое покраснение, которое проходит в течении нескольких часов. Процедура не требует реабилитационного периода и после сеанса пациент может продолжать привычный образ жизни.

<br>

<br>
<b>Частота проведения</b>: результаты будут видны уже после первой процедуры, но для закрепления и улучшения эффекта рекомендовано пройти курс из 4–6 сеансов с интервалом в две недели.
<br>

<br>
Фотоомоложение можно сочетать с другими косметологическими процедурами для получения более ярко выраженного эффекта, такими как контурная пластика, ботокс, комплексные уходы, пилинги; их последовательность и частота определяется специалистом.<br><br>


