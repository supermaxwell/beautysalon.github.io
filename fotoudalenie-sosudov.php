    <strong> Востановительный период после фотоудаления сосудов.</strong> <br>
Процедура фотоудаления сосудов требует незначительного реабилитационного периода. Зачастую на обработанном участке коже появляются покраснение, которое сходит в течение 2 дней. Иногда на месте бывших сосудистых звездочек образуется корочка или появляется темный отек, напоминающий синяк. Такие корочки ни в коем случаи нельзя срывать! Они самостоятельно отпадут в течение 2 недель. Для того что бы ускорить этот процесс можно наносить на место процедуры Бепантен или Пантенол, примерно 5-6 раз в день.
