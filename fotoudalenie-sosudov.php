                    <h3 class="section-title">Фотоудаление сосудов</h3>
                   <img class="img-circle"    src="dummy/sosud.jpg" alt=""/>

                     <b>Купероз и “сосудистые звездочки</b>” — это проблема, которая беспокоит сегодня многих людей, ведь каждая женщина мечтает о ровной, чистой, бархатистой и здоровой коже.
<br>
<br>
<b>Купероз или сосудистые звездоч</b>ки – это заболевание, которое проявляется в виде расширенных и/или лопнувших сосудов на коже. Причиной появления купероза является нарушение кровообращения в верхних слоях эпидермиса. Лопнувшие сосуды, которые обычно локализуются на бедрах, икрах, под коленками и на лице, образуют проявляющиеся в виде «прожилок» красного цвета, так называемые сосудистые звездочки.
<br>
<br>
Согласно статистике, сосудистые проблемы беспокоят около 40% взрослых людей после 20-25лет.
<br>
<br>
Купероз может быть как «самостоятельной» проблемой, так и симптомом  многих других заболеваний, наиболее склонная к куперозу сухая, тонкая и чувствительная кожа.
<br>
<br>
<h3 class="feature-title">Причины возникновения купероза:</h3>

    <strong>Вредные привычки.</strong> Курение и алкоголь пагубно воздействует на сердечно-сосудистую систему, и даже при незначительных дозах, употребления алкоголя возможен риск развития купероза и образования сосудестых звездочек.<br>

    <strong> Резкие перепады температуры</strong> очень негативно влияют на состояние кожи, и иногда даже однократное не сильное обморожение может привести к образованию купероза лице.<br>

    <strong>Генетическая предрасположенность</strong>. Вероятность возникновения купероза возрастает, если  в семье кто-либо из родителей страдает выраженными проявлениями купероза.<br>

    <strong> Излишнее УФ облучение. </strong>Пребывание под прямыми солнечными лучами, частое посещение солярия очень негативно влияют на нашу кожу, поражают стенки сосудов и тем самым провоцируют возникновение купероза.<br>

    <strong>Заболевания печени, почек, сердечно сосудистой систем</strong>ы (гипертоническая болезнь), а также эндокринологические заболевания могут стать причиной образования сосудистых звездочек.<br>

    <strong> Неправильный уход.</strong> Проведение агрессивных косметологических процедур могут истончать и обезвоживать кожу, наносить непосредственную травму её сосудистой сети или повышать восприимчивость к воздействию других факторов.<br>

    <strong> Авитаминоз.</strong> <br>
<strong>Стрессы.</strong><br>
<br>


Это заболевание не только предоставляет эстетические неудобства, но также и опасно, так как  провоцирует  раннее старение и негативные изменения структуры кожи.<br>

<h3 class="feature-title">Лечение купероза</h3>

 Сегодня современная косметология дает возможность нам эффективно боротся с любыми проявлениями купероза (судистые звездочки, сосудистые сеточки, винные пятна, гимангиомы и розацеа). На данный момент одним из наиболее результативных методов по устранению сосудистой сеточки, как на лице, так и теле считается ELOS-технология (или фотокоагуляция сосудов).
<br>
<br>

Сегодня лазерное и фотоудаление сосудов — являются самыми эффективными методами борьбы с сосудистыми образованиями.  Фото и лазерное удаление сосудов способно удалить не только красные, но и синие кровеносные сосуды.


<h3 class="feature-title">Суть процедуры фотоудаления сосудов.</h3>

 Суть метода заключается в воздействии высокоинтенсивного света на соединения гемоглобина в расширенных капиллярах, находящихся на поверхности. Мощный поток световой энергии, излучаемый установкой, нагревает гемоглобин в крови, что приводит к быстрому сворачиванию крови. Температура нагрева достаточно высока, что приводит к склеиванию сосудов. В результате сосуды становятся прозрачными и абсолютно не заметными под кожей.
<br>
<br>

Результат фотоудаления сосудов: чистая, ровная, гладкая и сияющая кожа без эстетических недостатков.
<br>
<br>

Сколько времени занимает процедура "фотоудаление сосудов"? В зависимости от типа и количества сосудов процедура может занимать от  20 до 40 мин.
<br>
<br>

Продолжительность курса. Фотоудаление сосудов может быть как однократной процедурой, так и проводится с интервалом в 3-4 недели. Необходимое количество процедур подбирается индивидуально лечащим врачом и зависит от состояния сосудов (чем  тоньше сосуд, тем меньше понадобится процедур). Зачастую может потребоваться от 2 до 4 процедур.<br>
<br>

