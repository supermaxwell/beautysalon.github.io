   <h3 class="feature-title">Эффект от процедуры</h3>

Технология фото не только убирает следы от акне (угрей) и снимает воспаление на коже, но и эффективно борется с причиной угревой болезни и прыщей - бактериями, которые находятся в закупоренных сальных железах и провоцируют воспаление. Кожа частично очищается уже несколько дней. Воспаление проходит, количество высыпаний заметно сокращается.
Технология выполнения
<br>
<br>
На предварительно очищенную кожу наносится специальный гель, по которому проводится сеанс лечения воспалительных процессов. Процедура достаточно комфортна и безболезненна, Вы можете почувствовать на коже тепло и легкое покалывание. По окончании лечения  обработанную кожу увлажняют или успокаивают специальным средством.
<br>
<br>
Длительность процедуры. В зависимости от области лечения процедура длится  30-45 минут.
<br>
<br>
Восстановительный период. Так как во время процедуры кожные покровы не повреждаются, периода реабилитации нет. По окончании сеанса Вы можете заниматься  запланированными ранее делами, не переживая о своей внешности.
<br>
<br>
Рекомендуемая частота проведения.  Эффект от процедуры проявляется постепенно. В зависимости от размеров и степени воспаления кожи, может потребоваться от 3 до 10 процедур. Процедура проводится 1 раз в неделю.
<br>
<br>
Рекомендуем  сочетать процедуру фотолечения акне с процедурами пилингов, мезотерапии, уходовыми процедурами по коже.
