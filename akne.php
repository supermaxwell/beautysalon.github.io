                    Считается, что прыщи – подростковая проблема. Но почему от них так часто страдают взрослые?
<br>
<br>
Акне (угри, акне вульгарис или в простонародье прыщи) – это изменения кожи, связанные с воспалением и нарушением работы сальных желез. Угри в основном появляются на участках кожи, где имеется много сальных желез: прежде всего, это лицо, грудь и спина. По статистике, угревой болезни подвержено до 80% людей в возрасте от 12 до 25 лет и примерно 30-40% — старше 25 лет.
<br>
<br>
Причины развития угревой болезни (акне):
<ul class="prices">
<li>гормональный дисбаланс;</li>
<li>бактерии Propionibacterium acnes ;</li>
<li>нарушение работы желудочно-кишечного тракта (ЖКТ);</li>
<li>хроническое воспаление носоглотки;</li>
<li>наследственность;</li>
<li>стресс;</li>
<li>применение определенных препаратов, например, кортикостероидов;</li>
<li>частое сдавливание кожи (например, телефонной трубкой) или привычка трогать лицо руками и др.</li>
</ul>

Отсюда главное правило: если у вас акне, идите  в профильную клинику, к дерматологу с достаточным опытом работы, который сможет поставить корректный диагноз и назначить правильное лечение. Только в этом случае можно рассчитывать на положительный стойкий результат.
<br>
<br>
Лечение акне. В лечении угревой сыпи достаточно эффективна процедура фототерапии акне. Она подавляет рост бактерии Propionibacterium acnes, снимает воспалительный процесс в коже и оказывает отшелушивающее действие.


