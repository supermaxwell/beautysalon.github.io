                                        <li>мелазмы.</li>

</ul>
<b>Эффект от процедуры: </b>уже после нескольких  сеансов Вы сможете увидеть результат: сияющую, чистую и здоровую кожу без каких-либо дефектов. <br>
<br>
Технология выполнения удаления пигментных пятен. В нашем медицинском центре  процедура проводится при помощи аппарата NannoLight™ IPL System - наиболее востребованной многофункциональной IPL-системы, которая удовлетворит Ваши самые высокие требования.  Процедура фотоудаления пигментных пятен не требует предварительной подготовки. Вам дадут  защитные очки, на проблемную область нанесут специальный гель и обработают участок кожи необходимым количеством вспышек.  При этом поверхность кожи не повреждается, процедура совершенно асептична.
<br>
<br>
<b>Длительность процедуры:</b> в зависимости от общей площади обрабатываемого участка кожи процедура может занять от 15 до 60 минут.
<br>
<br>
<b>Восстановительный период:</b> процедура максимально комфортна и безболезненна, поэтому не имеет периода реабилитации и не нарушает ежедневного графика жизни.
<br>
<br>
<b>Частота проведения процедур:</b> курс  назначает врач, количество сеансов подбирается индивидуально в зависимости от выраженности пигментации, возраста, чувствительности кожи, особенностей организма.  Для стойкого результата обычно требуется от 3 до 10 процедур с интервалом 3-4 недели.
<br>
<br>
Впоследствии образование новых пигментных пятен не исключено, если не будет устранена вызвавшая их причина. Но и в таком случае процесс накопления меланина длится дольше, чем перед прохождением курса терапии, новые пигментные пятна не обладают такой интенсивностью.
