						<p> <b>Фотоудаление пигментного пятна</b> – это процедура устранения пигментного образования (продукт выработки меланина) на коже лица, тела, конечностях с помощью IPL-технологии.</p>
<h3 class="feature-title">Суть метода</h3>

 <b>Суть процедуры  фотоудаления пигментных пятен по технологии IPL </b> заключается в воздействии на кожу интенсивного импульсного света с длиной волны от 400 до 1400 нм., который проникая внутрь, разрушает меланин, который впоследствии выводится естественным путем. Благодаря этому процессу, участки с пигментными пятнами сначала темнеют, а потом отшелушиваются.  Процедура не осветляет пятна и не превращает их в белые — они просто исчезают.
<h3 class="feature-title">Показания</h3>

<ul class="prices">
