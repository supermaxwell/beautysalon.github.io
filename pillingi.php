<h3 class="feature-title">Глубокий пилинг</h3>
Глубокий пилинг - наиболее сложная процедура. Она считается дерматохирургической, так как при ее проведении повреждается сосочковый слой дермы. При этом область обработки ограничена параорбитальной и пероральной зонами, на остальных делается срединный пилинг, зато в результате у клиента значительно улучшается состояние кожи.<br>
<br>
Для процедуры используется фенол или 35-50% ТСА. Результатом процедуры, проведенной фенолом, является выраженный эффект лифтинга всего овала лица. Однако это вещество очерь токсично и может вызвать почечную и сердечную недостаточность. После фенолового пилинга возможны такие осложнеия, как инфекционные и воспалительные процессы, гиперпигментация и депигментация.
