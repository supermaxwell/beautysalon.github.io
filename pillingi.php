Еще недавно глубокий пилинг фенолом проводился под наркозом из-за своей болезненности. При этом было необходимо присутствие реанимационной бригады и кардиомониторинг. Естественно, что подобные условия сложно соблюдать в условиях салона красоты, поэтому глубокий пилинг-явление редкое. Однако сейчас появились феноловые пилинги нового поколения, которые не требуют подобных мер предосторожности, и, возможно, в ближайшее время ситуация изменится. Пока что в салоне чаще можно встретить услугу срединно-глубокого пилинга, чем просто глубокого.	<br>
<br>
В этом отношении глубокий пилинг ТСА предпочтительней. Он не так опасен, хотя тоже требует анестезии. При его неправильном проведении также возможны нежелательные последствия, такие как возникновение демаркационной линии и образование рубцов.	<br>
<br>
После глубоких пилингов реабилитационный период может продлиться до 2-3 месяцев.


