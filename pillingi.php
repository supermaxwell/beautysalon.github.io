 <strong>Ретиноевый пилинг </strong><br>

Ретиноевый пилинг разными специалистами относится как к поверхностному, так и к срединному. Это разногласие связано с тем, что по травматичности он не опасен, а по оказываемому эффекту - сравним со срединными пилингами. Ретиноевый пилинг применяется для коррекции инволюционных изменений кожи. Он может проводиться даже при наличии диагноза "купероз", тогда как другие виды пилинга при нем применять не рекомендуется.<br>
<br>
Однако в случае с ярко выраженными мимическими морщинами ретиноевый пилинг недостаточно эффективен. Его недостаток - эмбриотоксическое и гепатотоксическое действие, поэтому процедура не проводится беременным и кормящим женщинам.
