<ul class="prices">



    <li>морщины в области лба и на переносице;</li>
    <li>межбровные морщины,</li>
    <li>“гусиные лапки” (морщины вокруг глаз)</li>
    <li>гипергидроз (повышенное потоотделение) в области подмышек, на ладонях и ступнях.</li>
</ul>

   <h3 class="feature-title"> С помощью инъекций Ботокса / Диспорта достигается видимый результат:</h3>
<ul class="prices">
    <li>эффективная коррекция мимических морщин;</li>
  <li>  длительное расслабляющее действие (3-12мес.);</li>
    <li>коррекция внешнего вида: легкое поднятие бровей и уголков губ;</li>
    <li>устранение чрезмерного потоотделения</li>
    <li>возможность избавления от привычки хмуриться, морщиться.</li>

</ul>

 <strong>Технология выполнения:</strong> перед инъекцией ботокса (диспорта) обязательно проводится консультация, в ходе которой врач оговаривает с пациентом основные цели инъекции, желаемые результаты, обсуждаются противопоказания и возможные осложнения. Подобрав оптимальную дозу препарата (необходимое количество единиц) в соответствии с полом и возрастом, доктор начинает процедуру.
<br>
<br>

Технология проведения процедуры является классической. С помощью инсулинового шприца препарат (ботокс или диспорт) вводится непосредственно в мышцу.
<br>
<br>

Во время процедуры пациент может испытывать незначительный и кратковременный дискомфорт.
<br>
<br>

Важно отметить, что открытый флакон препарата хранится недолго, потому обычно для инъекций ботулотоксина собирается группа людей под которую открывается флакон.
<br>
<br>

 <strong>Длительность процедуры:</strong> инъекции ботокса (диспорта) несложные и длятся в среднем 10-20 минут.
<br>
<br>

 <strong>Восстановительный период:</strong> процедура введения Ботокса проводится в амбулаторных условиях, обычно без анестезии. Стационарное наблюдение не требуется. По окончании процедуры в местах инъекций остаются небольшие бугорки, которые бесследно расходятся через 20-30 минут, поэтому уже сразу после процедуры можно возвращаться к нормальной жизни.
<br>
<br>



