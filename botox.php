                    <h3 class="section-title">Инъекции ботокса, диспорта</h3>
                   <img class="img-circle"    src="dummy/botox.jpg" alt=""/>

                       Каждая женщина пытается надолго сохранить молодость и красоту, однако время не стоит на месте и с годами на нашей коже образуется все больше и больше морщин.

Старение – это неизбежный процесс, и мы никак не можем предотвратить его. К счастью, современная косметология предлагает множество способов для коррекции и устранения морщин  и продления нашей молодости.
<br>
<br>

Одним из наиболее эффективных методов борьбы и удаления морщин являются инъекции ботокса или его аналога диспорта.
<br>
<br>

                       <strong>Диспорт и Ботокс</strong> это ботулинический токсин типа А, периферический миорелаксант пролонгированного действия. «Диспорт» и «Ботокс» являються разными фирменными названиями одного и того же препарата.
<br>
<br>

                       <strong>Препарат «Диспорт»</strong> – производится французской компанией Beaufour Ipsen International, а их производственные мощности (завод) находится в Англии.
<br>
<br>

                       <strong>«Ботокс»</strong> выпускает американская фирма Allergan. Принципиальная разница между Ботоксом и Диспортом заключается лишь в концентрации ботулотоксина и его способности проникать не только в «целевую» мышцу, но и в соседние с ней мышцы лица.
<br>
<br>

Так как препарат вводится инъекциями непосредственно в мимические мышцы, последствия некорректного ввода препарата могут быть неприятными поэтому правом введения препарата обладают только врачи, прошедшие специальное обучение и имеющие сертификат компании-производителя. Наши врачи имеют все необходимые сертификаты.

