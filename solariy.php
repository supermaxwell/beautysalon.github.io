Загар - это ничто иное, как естественная защита кожи от солнечных лучей. Под воздействием UV-B лучей меланоциты (клетки эпидермиса) начинают продуцировать больше белых меланиновых пигментов, которые впоследствии , продвигаяясь ближе к поверхности кожи, окрашиваются в коричневый цвет UV-A лучами (прямая пигментация) и кислородом, содержащимся в крови (косвенная пигментация).
