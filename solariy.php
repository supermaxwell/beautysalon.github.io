<h3 class="feature-title">Правила посещения солярия</h3 >

Основное правило для начинающих посещать солярий постепенность и здравый смысл. Не следует спешить, чтобы пробрести интенсивный загар. Нельзя пользоваться солярием 2 раза в день. После того, как приобретена желаемая интенсивность загара, её можно поддерживать, принимая 1-2 сеанса в неделю. Граница индивидуальной переносимости достигнута, если через несколько часов после инсоляции наступает легкое покраснение и напряженность кожи. Время инсоляции в этом случае следует сократить.

