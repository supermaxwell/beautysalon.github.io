<h3 class="feature-title">Программа загара</h3>

Определив свой тип кожи и проконсультировавшись с врачом (при наличии заболеваний) можно приступать к загару.
<br>
<br>

Количество сеансов для людей с I и II типом кожи не должно превышать 8. Для остальных фототипов их число можно увеличить до 10-12.
Продолжительность первого сеанса должна быть равна половине максимального времени облучения (зависит от модели солярия и типа ламп). Её можно уточнить у администратора салона.  После первого дня делается перерыв на сутки, чтобы удостовериться в отсутствии негативных реакций на облучение. 
<br>
<br>

Второй сеанс может длиться уже в полтора раза дольше первого. При условии хорошей переносимости, третий и все последующие сеансы можно принимать ежедневно. Людям с I и II типами кожи, учитывая их чувствительность к облучению, очень важно не превышать максимальное время загара во избежание негативных реакций.  Людям, относящимся к III-IV фототипам, можно немного увеличить продолжительность сеансов (начиная с пятого), в том случае, если они считают свой загар не слишком интенсивным. Необходимо делать это очень осторожно, не превышая максимальное время загара более чем на 50%. Людям с загорелой или смуглой кожей для получения более интенсивного и красивого загара рекомендуется применять активаторы загара. В дальнейшем, для поддержания загара можно посещать солярий в течение 2 месяцев 1-2 раза в неделю.

<h3 class="feature-title">Возрастные ограничения</h3>

Детям до 15 лет рекомендуется соблюдать особую осторожность при загорании в солярии. 
Пожилые люди получают пользу от приема солнечных ванн и у них часто проходят мышечные боли и ревматические явления. Важно лишь правильно определить режим загорания с учетом всех физиологических и возрастных особенностей.
<br>
<br>

<h3 class="feature-title">Когда лучше принимать душ, до или после солярия?</h3>

Поскольку мыло влияет на естественную жирность кожи, рекомендуется не принимать душ или ванну с использованием мыла непосредственно до сеанса в солярии. 
<br>
<br>

Применение специальных косметических средств для загара в солярии - крем для загара
<br>
<br>

Персональная программа сеансов загорания в солярии основана на индивидуальном типе кожи и при этом всегда предусматривается, что на кожу не нанесены никакие мази или кремы. Средства защиты от солнечного ожога, мази и кремы для загара разлаживают вашу программу загорания, что делает её не надежной. Это не относится к кремам, специально разработанным для использования в соляриях. Те, у кого кожа отличается сухостью, часто предпочитают использовать гидрирующий крем до и после принятия солнечной ванны. Как говорилось выше средства парфюмерии могут содержать вещества, которые повышают чувствительность кожи; однако, эти вещества могут также присутствовать в средствах для макияжа и косметических продуктах. Поэтому мы советуем тем, кто пользуется соляриями, не делать макияж и не пользоваться косметикой во время загорания в солярии.

<h3 class="feature-title">Виды  кремов для солярия</h3>

<strong>Ускоритель загара:</strong> этот лосьон используют на первом этапе пребывания в солярии. Он способствует образованию базового загара. Зачастую в состав средства входит много увлажняющих компонентов, ведь хорошо увлажнённая кожа не только загорает быстрее и лучше, но и дольше сохраняет желаемый оттенок.
<br>
<br>

<strong>Усилитель загара:</strong> лосьон рассчитан на второй этап приобретения искусственного загара, когда база уже есть, но необходимо её усилить. В нём не очень много увлажняющих ингредиентов, поэтому наносят средство прямо перед сеансом в солярии.
<br>
<br>

<strong>Крем с тингл-эффектом:</strong> особенный тип лосьона, которые многие производители относят к третьему этапу приобретения искусственного загара. Чаще всего тингл-эффект встречается в лосьонах-усилителях, которые содержат специальные компоненты, вызывающие покраснение и увеличивающие приток крови к коже. Они способствуют обновлению клеток кожи, что обеспечивает более быстрый загар. Средства с тингл-эффектом вызывают чувство щипания или покалывания, поэтому не рекомендованы для людей с чувствительной кожей или тем, кто плохо переносит подобные физические ощущения.
<br>
<br>

<strong>Бронзатор:</strong> нечто среднее между ускорителем и усилителем загара. Изначально это было средством для автозагара, которое приобрело форму лосьона, поэтому гарантирует мгновенный результат. Вся хитрость в том, что кожа практически сразу меняет цвет, но продолжает загорать под бронзатором. Благодаря этому, тело приобретает равномерный оттенок без изъянов в виде полос или оранжевых пятен.
<br>
<br>

<strong>Лосьон с эффектом Body Blush: </strong>он действует по принципу косметического продукта с тингл-эффектом, но не вызывает на коже ощущение тепла и лёгкого покалывания. Средство предназначено специально для людей, обладающих чувствительной кожей.
<br>
<br>

<strong>Омолаживающий лосьон:</strong> в состав этого продукта входят особые вещества и витамины, предотвращающие преждевременное старение кожи. <strong>Лосьон-шиммер:</strong> это средство придаёт коже изысканный блеск, она в прямом смысле сияет.



