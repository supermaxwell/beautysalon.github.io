<h3 class="feature-title">Противопоказания.</h3>

Процедуры контурной пластики нельзя проводить при беременности, в период кормления грудью, во время менструации, а также людям с аллергическими реакциями на используемые препараты и их компоненты. Также противопоказанием является склонность кожи к келоидному или гипертрофическому рубцеванию, вирусные и грибковые заболевания кожи, обострение соматических, инфекционных, аутоиммунных заболеваний, гемофилия, сахарный диабете I и II типа, нарушение свертываемости крови и прием антикоагулянтов.
<br><br>

Контурная пластика эффективна как самостоятельная методика, но может дополняться и другими салонными процедурами – мезотерапией, инъекциями ботулотоксина, косметическими масками.


