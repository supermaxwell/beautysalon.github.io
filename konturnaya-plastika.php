 <h3 class="feature-title">Технология выполнения.</h3>

Перед началом проведения контурной пластики необходима консультация косметолога, в ходе которой обсуждаются цель процедуры, ожидаемый эффект, выясняются есть ли у клиента противопоказания к данной  процедуре и подбирается препарат.
<br>
<br>

Если противопоказаний к контурной пластике у Вас не наблюдается, косметолог приступает к осуществлению процедуры. Вначале врач обозначает зону введения препарата и в случае необходимости наносит  анестетик,  затем при помощи шприца с тонкой иглой доктор  вводит в проблемную зону  специальный препарат — филлер.
<br>
<br>

Небольшой эффект Вы увидите уже на следующий день, а через неделю Вы получите то, что хотели, - гладкую кожу без морщин.
<br>
<br>

Осложнения после процедуры контурной пластики могут быть, поэтому очень важно, чтобы  процедуру осуществлял квалифицированный косметолог, знающий  анатомию лица и владеющий методикой подбора и введения филлеров!
Длительность процедуры колеблется в зависимости от зоны от 30 минут и дольше.
<br>
<br>

Восстановительный период. Сразу после процедуры пациент может возвращаться к своим обычным делам. На лице будет заметна небольшая припухлость в области введения инъекций, которая спадет через пару дней.
