                    <h3 class="section-title">Контурная пластика</h3>
                   <img class="img-circle"    src="dummy/kontur.jpg" alt=""/>

                      <strong>Контурная пластика</strong> – это инъекционная косметологическая процедура омоложения и коррекции дефектов (например: разглаживание мимических и глубоких морщин,  изменение контуров и овала лица, увеличение губ и др.).  Процедура происходит путем введения внутрикожно препаратов-наполнителей (филлеров), благодаря которым создается необходимый подкожный объем и разглаживаются  имеющиеся складки, морщины и рубцы. Филлер – в переводе с английского, наполнитель. Процедура контурной пластики относится к числу наиболее востребованных в косметологии всего мира.<br>
<br>
 <h3 class="feature-title">Увеличение губ</h3>

Коррекция или увеличение губ  инъекционными препаратами на основе гиалуроновой кислоты -  одна из наиболее популярных процедур. Вы получите идеальную форму губ! Длительность процедуры увеличения губ занимает порядка 10 – 30 минут, период реабилитации после этого минимален.
<br>
<br>

<strong>Преимущества:</strong> <br>
<ul class="prices">

<li>• Моментальный результат;</li>
<li>• Отсутствие длительной реабилитации;</li>
<li>• Долговременное сохранение косметического эффекта;</li>
<li>• Возможность малоинвазивной коррекции некоторых эстетических проблем без хирургического вмешательства</li>
</ul>


<strong>Препараты для контурной пластики.</strong> <br>
<br>
Международные правила производства лекарственных средств предъявляют к препаратам для для контурной пластики высокие стандарты качества и безопасности. Все используемые вещества в препарате подлежат строгому контролю. Филлеры должны обладать биосовместимостью с тканями, высокой степенью стерильности, очищенности от эндотоксинов, прозрачностью и другое.
Препараты, используемые в контурной пластике делятся на несколько групп, в зависимости от времени нахождения в тканях:
<br>
<br>

1)  биодеградируемые – полностью рассасываются в течение 4 – 18 мес. Эти импланты, как правило, имеют в своей структуре один компонент, выступающий в качестве наполнителя. Чаще всего это стабилизированная гиалуроновая кислота или коллаген.
<br>
<br>

2) частично биодеградируемые (длительно действующие) – рассасываются в течении  от 1,5 – 2,5 лет.
<br>
<br>

3) бионедеградируемые (перманентные  или постоянные). Находятся в тканях от 5 и более лет. Обобщенный и часто используемый термин для имплантов этой группы – «силикон».
<br>
<br>

Преимущества биодеградируемых препаратов для контурной пластики состоит в низком риске осложнений и отсутствии миграции материалов; основным же недостатком служит относительная кратковременность действия (от нескольких месяцев до 1-2 лет).


