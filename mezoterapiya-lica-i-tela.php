                   <img class="img-circle"    src="dummy/mezoterapiya.jpg" alt=""/>

                      Мезотерапия, как метод оздоровления, была изобретена в 1958 году французским доктором Мишелем Пистором, а сегодня этот эффективный медицинский и косметологический метод признан во всём мире<br>
<br>


<strong>Мезотерапия</strong> – это современная методика оздоровления и омоложения кожи  посредством микроинъекций специальных препаратов. <br>
<br>
<h3 class="feature-title">Возможные ореолы проведения процедуры:</h3>

    <ul class="prices">
   <li> лицо –  омоложение и лечение различных эстетических недостатков;</li>
    <li>кожа головы (волосы) – терапия против выпадения волос, стимулирования их роста;</li>
    <li>тело – лечение целлюлита и значительных жировых отложений.</li>
    </ul>

Лечебные вещества доставляются клеткам адресно и начинают действовать немедленно, тем самым быстро воздействуя на проблемную зону кожи.  Помимо фармакологического, процедура имеет акупунктурный эффект.
<br>
<br>

В своей практике косметология использует свыше 3000 разновидностей коктейлей для мезотерапии; вопрос о выборе и применении того или иного состава решает дерматокосметолог.

<h3 class="feature-title">Показания для применения процедуры.</h3>

<ul class="prices">

    <li>Атония (обвислая, увядающая, не эластичная, морщинистая кожа);</li>
    <li>Гиперпигментация кожи, в том числе, веснушки и пигментные пятна (включая сезонные);</li>
    <li>Целлюлит, локальные  жировые отложения (на животе, бедрах, руках и т.д.);</li>
   <li> Изменения формы и овала лица (птоз, второй подбородок, обвислые щеки , отеки);</li>
    <li>Излишне сухая или жирная, пористая кожа;</li>
    <li>Купероз, акне вне стадии обострения, розацеа</li>;
   <li> Подготовка и реабилитация после хирургических операций;</li>
    <li>Подготовка и реабилитация после лазерной шлифовки кожи.</li>

</ul>


