   <li> Ровный тон кожи, эффект свежести и «сияния изнутри»;</li>
    <li>Чистая кожа без угрей и других эстетических несовершенств различного происхожде</li>ния;
   <li> Гладкие, здоровые, блестящие волосы;</li>
    <li>Четкий, изящный контур тела.</li>

</ul>


<h3 class="feature-title">Технология выполнения.</h3>

  После консультации, собрав все необходимые сведения о состоянии Вашего здоровья, Ваших пожеланиях и предпочтениях, косметолог назначит курс «инъекций красоты», подобранный индивидуально по вашему типу кожи. Обычно специальной подготовки пациента, перед прохождением процедуры не требуется. Рекомендуется только за 1-3 дня до процедуры воздержаться от приёма препаратов, которые способны повлиять на свёртываемость крови (в их числе — аспирин, некоторые обезболивающие и антибиотики).
<br>
<br>

В нашем центре процедура введения мезопрепаратов проводится вручную, при помощи шприца и специальной мезоиглы. Мы осознанно не используем специальные мезоинжекторы (специальные аппараты для введения коктейля), поскольку с нашей точки зрения проблемные зоны на лице и теле требуют более точного и аккуратного введения инъекций. Такой способ введения препарата даёт возможность ювелирной манипуляции на лице (зона вокру глаз и носа), к тому же снижаются риски травмировать мелкие сосуды.
<br>
<br>

Процедура  малоболезненна, но по желанию клиента врач предложит  использовать специальный анестетик, который сделает кожу менее чувствительной и процедура пройдет легко и незаметно.
<br>
<br>

<strong>Длительность процедуры</strong> колеблется от нескольких минут до одного часа. Время зависит от размера обрабатываемой зоны. <br>
<br>

<strong>Восстановительный период.</strong> После процедуры может ненадолго возникнуть небольшое покраснение кожи,  но оно очень быстро проходит. <br>
<br>

<strong>Рекомендуемая частота проведения.</strong> В зависимости от исходного состояния кожи, зоны лечения и исходных целей, количество процедур варьируется от трех до восьми с интервалом 1 неделя. Чтобы результат сохранялся дольше, врачи рекомендуют поддерживающие сеансы, примерно 1 раз в месяц. Если случай достаточно сложный, через несколько месяцев может быть рекомендован повторный курс.<br>

